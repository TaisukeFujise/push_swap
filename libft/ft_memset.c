#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*dest;
	size_t	i;

	dest = (char *)b;
	i = 0;
	while (i < len)
	{
		dest[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
