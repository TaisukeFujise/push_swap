#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*src;
	char	*dst;
	size_t	i;

	src = (char *)s;
	i = 0;
	dst = malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!dst)
		return (NULL);
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
